    public void cargarTabla() {
        this.tabla.setVisible(true);
        this.doctores = DoctorDAO.getDoctores();
        String[] row = new String[3];
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        for (Doctor doctor : this.doctores) {
            row[0] = Integer.toString(doctor.getCedula());
            row[1] = doctor.getEspecialidad();
            row[2] = String.format("%s %s %s", doctor.getNombre(), doctor.getApellidoPaterno(),
                    doctor.getApellidoMaterno());
            model.addRow(row);
        }
    }

